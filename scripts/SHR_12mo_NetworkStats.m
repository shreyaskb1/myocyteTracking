function [seg] = addpoints(seg,list)
    for i=1:size(seg,1)
        seg(i,2)=list((seg(i,1)),1);
        seg(i,3)=list((seg(i,1)),2);
        seg(i,4)=list((seg(i,1)),3);
    end
end

function [seg] = pointstolist(points)
k=0;
seg=zeros(1,4);
    for i=1:length(points)
        for j=1:length(points{i})
            k=k+1;
            seg(k,1)=points{i}(j);
        end
    end
end

