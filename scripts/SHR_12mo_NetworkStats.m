% This function calculates the branching statistics for the SHR 12mo
% cardiomyocyte network.  This was used for the Analysis of
% location-dependent cardiomyocyte branching for FIMH 2021
% Alexander Wilson wilsonaj@stanford.edu

%################ Length Statistics

list = call_list();
[points, num_cells] = call_points();
layer = layerdata();
seg=cell(1,num_cells);
image_resolution=1; %1 micron

for i=1:4
    cell_length(i)=0;
end

for i=1:num_cells        
    seg{i}=pointstolist(points{i});
    seg{i}=addpoints(seg{i},list);
    
    for j=1:4
        if layer(i,2)==j
            cell_length(j)=cell_length(j)+abs(seg{i}(1,4)-seg{i}(size(seg{i},1),4));
        end
    end
    
end
        
cell_length=cell_length*image_resolution;

%################ Branching Statistics

Two_bps = BranchPointsPairs();
Three_bps = BranchPointsTrips();

for i=1:4
    Within_Stat(i)=0;
    Between_Stat(i)=0;
end

%Do the branching statistics for the branching pairs

for i=1:(length(Two_bps)/2)
    cell_1=Two_bps(2*i-1,1);
    cell_2=Two_bps(2*i,1);
    
    Layer_1=layer(cell_1,2);
    Layer_2=layer(cell_2,2);
    
    for i =1:4
        Within_Stat(i)=Within_Stat(i)+2*double(Layer_1==i&Layer_2==i);
        Between_Stat(i)=Between_Stat(i)+double((Layer_1==i&Layer_2~=i)|(Layer_1~=i&Layer_2==i));
    end
end

%Do the branching stats for the branching trips

for j=1:(length(Three_bps)/3)
    cell_1=Three_bps(3*j-2,1);
    cell_2=Three_bps(3*j-1,1);
    cell_3=Three_bps(3*j,1);
    Layer_1=layer(cell_1,2);
    Layer_2=layer(cell_2,2);
    Layer_3=layer(cell_3,2);
    
    for i = 1:4
        Within_Stat(i)=Within_Stat(i)+(sum([Layer_1==i,Layer_2==i,Layer_3==i])>1)*sum([Layer_1==i,Layer_2==i,Layer_3==i]);
        Between_Stat(i)=Between_Stat(i)+(Layer_1~=i|Layer_2~=i|Layer_3~=i)*sum([Layer_1==i,Layer_2==i,Layer_3==i]);
        Branch_Results(i*2-1).name="Within_Stat" + string(i);
        Branch_Results(i*2-1).value=Within_Stat(i);
        Branch_Results(i*2-1).bylength=Branch_Results(i*2-1).value/cell_length(i);
        Branch_Results(i*2).name="Between_Stat" + string(i);
        Branch_Results(i*2).value=Between_Stat(i);
        Branch_Results(i*2).bylength=Branch_Results(i*2).value/cell_length(i);
    end
end

Total_Branching(1:3)=Between_Stat(1:3)./(cell_length(1:3)/100);
Total_Branching(4:6)=Within_Stat(1:3)./(cell_length(1:3)/100);

Total_Branching_av=mean(Total_Branching)
Total_Branching_SE=std(Total_Branching)/sqrt(6)

